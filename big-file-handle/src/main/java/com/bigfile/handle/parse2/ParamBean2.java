package com.bigfile.handle.parse2;

/**
 * @author WuLiangzhi  2019/06/17 21:57
 */
public class ParamBean2 {

    private String prefix_key;
    private String key;
    private String value_key;
    private String format_value;

    /**
     * 获取 prefix_key 属性
     *
     * @return the value of prefix_key
     */
    public String getPrefix_key() {
        return prefix_key;
    }

    /**
     * 设置 prefix_key 属性
     *
     * @param prefix_key prefix_key
     */
    public void setPrefix_key(String prefix_key) {
        this.prefix_key = prefix_key;
    }

    /**
     * 获取 key 属性
     *
     * @return the value of key
     */
    public String getKey() {
        return key;
    }

    /**
     * 设置 key 属性
     *
     * @param key key
     */
    public void setKey(String key) {
        this.key = key;
    }

    /**
     * 获取 value_key 属性
     *
     * @return the value of value_key
     */
    public String getValue_key() {
        return value_key;
    }

    /**
     * 设置 value_key 属性
     *
     * @param value_key value_key
     */
    public void setValue_key(String value_key) {
        this.value_key = value_key;
    }

    /**
     * 获取 format_value 属性
     *
     * @return the value of format_value
     */
    public String getFormat_value() {
        return format_value;
    }

    /**
     * 设置 format_value 属性
     *
     * @param format_value format_value
     */
    public void setFormat_value(String format_value) {
        this.format_value = format_value;
    }
}
