package com.bigfile.handle.parse2;

/**
 * @author WuLiangzhi  2019/06/17 21:57
 */
public class ResultBean2 {

    private String key;
    private String value;
    private String time;
    private String format_value;

    /**
     * 获取 key 属性
     *
     * @return the value of key
     */
    public String getKey() {
        return key;
    }

    /**
     * 设置 key 属性
     *
     * @param key key
     */
    public void setKey(String key) {
        this.key = key;
    }

    /**
     * 获取 value 属性
     *
     * @return the value of value
     */
    public String getValue() {
        return value;
    }

    /**
     * 设置 value 属性
     *
     * @param value value
     */
    public void setValue(String value) {
        this.value = value;
    }

    /**
     * 获取 time 属性
     *
     * @return the value of time
     */
    public String getTime() {
        return time;
    }

    /**
     * 设置 time 属性
     *
     * @param time time
     */
    public void setTime(String time) {
        this.time = time;
    }

    /**
     * 获取 format_value 属性
     *
     * @return the value of format_value
     */
    public String getFormat_value() {
        return format_value;
    }

    /**
     * 设置 format_value 属性
     *
     * @param format_value format_value
     */
    public void setFormat_value(String format_value) {
        this.format_value = format_value;
    }

}
